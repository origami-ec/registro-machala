package org.bcbg.entities;

import java.io.Serializable;
import java.math.BigInteger;

/**
 *
 * @author Anyelo
 */
public class SecuenciaGeneral implements Serializable {

    private Long id;
    private String code;
    private BigInteger secuencia;
    private Integer anio;

    public SecuenciaGeneral() {
    }

    public SecuenciaGeneral(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public BigInteger getSecuencia() {
        return secuencia;
    }

    public void setSecuencia(BigInteger secuencia) {
        this.secuencia = secuencia;
    }

    public Integer getAnio() {
        return anio;
    }

    public void setAnio(Integer anio) {
        this.anio = anio;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SecuenciaGeneral)) {
            return false;
        }
        SecuenciaGeneral other = (SecuenciaGeneral) object;
        return !((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id)));
    }

    @Override
    public String toString() {
        return "com.origami.sgr.entities.SecuenciaGeneral[ id=" + id + " ]";
    }
}
